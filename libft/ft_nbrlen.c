unsigned int	ft_nbrlen(long int n)
{
	if (n < 0)
	{
		if (n == -2147483648)
			return (11);
		n *= -1;
		return (ft_nbrlen(n) + 1);
	}
	if (n < 10)
		return (1);
	return (ft_nbrlen(n / 10) + 1);
}
